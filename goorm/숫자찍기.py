def extend(n, size):
  ret = []
  num = numList[int(n)]
  for i in range(len(num)):
    row = [num[i][0]]
    for _ in range(size):
      row.append(num[i][1])
    row.append(num[i][2])
    
    if i%2 == 0:
      ret.append(row[:])
    else:
      for _ in range(size):
        ret.append(row[:])

  return ret
        
